
class RaceResult {
  final int rank;
  final String bib;
  final String name;
  final String cycleTime;
  final String runTime;
  final String swimTime;
  final String duration;

  RaceResult({
    required this.rank,
    required this.bib,
    required this.name,
    required this.cycleTime,
    required this.runTime,
    required this.swimTime,
    required this.duration,
  });
}